#Quotation Manipulation
#demonstrates string methods

#quote from IBM Chairman, Thomas Watson, in 1943.
quote = "\"I think there is a world market for maybe five computers.\""

print("Original quote: ", quote)

print("\nIn UPPERCASE: ", quote.upper())

print("\nIn lowercase: ", quote.lower())

print ("\nIn Title: ", quote.title())

print("\nWith a minor replacement: ", quote.replace("five", "millions of"))

print("\nOriginal quote is still: ", quote)

input("\n\nPress the enter key to exit.")
